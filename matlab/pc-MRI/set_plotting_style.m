function fig_opts = set_plotting_style



    %figPos = get(0, 'defaultfigureposition');
    %fig_opts.Position = [figPos(1), figPos(2), 640, 360];

    fig_opts.Color = 'white';
    fig_opts.defaultAxesBox = 'off';
    fig_opts.defaultAxesColor = 'white';
    %fig_opts.defaultAxesInterpreter = 'none';
    fig_opts.defaultAxesFontName = 'Helvetica';
    fig_opts.defaultAxesFontUnits = 'points';
    fig_opts.defaultAxesFontSize = 10;
    fig_opts.defaultAxesUnits = 'normalized';
    fig_opts.defaultAxesVisible = 'on';
    fig_opts.defaultLineColor = [0.00 0.00 0.00];
    fig_opts.defaultLineLineStyle = '-';
    fig_opts.defaultLineLineWidth = 1.5;
    fig_opts.defaultLineMarker = 'none';
    fig_opts.defaultLineMarkerSize = 8;
    fig_opts.defaultLineMarkerFaceColor = 'auto';
    fig_opts.defaultTextInterpreter = 'none';
    fig_opts.defaultTextColor = [0.00 0.00 0.00];
    fig_opts.defaultTextFontName = 'Helvetica';
    fig_opts.defaultTextFontUnits = 'points';
    fig_opts.defaultTextFontSize = 10;
    fig_opts.defaultTextFontWeight = 'normal';
    fig_opts.defaultTextVerticalAlignment = 'middle';
    fig_opts.defaultTextHorizontalAlignment = 'left';
    fig_opts.defaultAxesTitleFontWeight = 'normal';
    fig_opts.defaultAxesTickLength = [0.01 0.02];
    fig_opts.defaultAxesTickDirMode = 'manual';
    fig_opts.defaultAxesTickDir = 'out';
    fig_opts.defaultAxesLabelFontSizeMultiplier = 1.25;
    fig_opts.defaultAxesTitleFontSizeMultiplier = 1.25;
    fig_opts.defaultTextRotation = 0;
    fig_opts.defaultLegendBox = 'off';
    fig_opts.defaultLegendFontSize = 10;
    fig_opts.defaultLegendFontSizeMode = 'manual';


    
end
